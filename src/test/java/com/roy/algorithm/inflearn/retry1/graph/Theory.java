package com.roy.algorithm.inflearn.retry1.graph;

public class Theory {
    // Graph (V, E): Vertex와 Edge로 이루어진 집합
    // Vertex: 노드를 칭함
    // Edge: 노드와 노드를 연결하는 간선

    // 1. 무방향 그래프
    // Vertex와 Edge가 방향없이 연결된 그래프.
    // 1 2 이러한 식으로 입력이 되면 1번 Vertex(노드)와 2번 Vertex(노드)가 연결되어 있다는 의미

    // 2. 방향 그래프
    // Vertex를 연결하는 Edge에 방향이 있는 그래프.
    // 표를 그릴 때 행 -> 열로 이동이 가능하다고 이해한다.

    // 3. 가중치 방향 그래프
    // Vertex를 연결하는 Edge에 방향과 비용이 있는 그래프.
    // 표를 그릴 때 행 -> 열로 이동이 가능하다고 이해한다.
    // 또한 표를 그릴 때 값은 가중치가 되어야한다.
}
